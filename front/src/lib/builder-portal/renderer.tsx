import type { BuilderNode } from '../builder-core'

type RenderOptions = {
  /** Replaces <page-content /> nodes with this slot content (used by layouts). */
  pageContent?: ReactNode
  /**
   * When true, the tree is rendered for the live portal (not the builder
   * preview), so inputs accept user input instead of being disabled.
   */
  runtime?: boolean
  /**
   * Real identity providers fed by the realm's login settings — replaces the
   * placeholder list rendered for `identity_providers` blocks in the canvas.
   */
  identityProviders?: Schemas.IdentityProviderPresentation[]
}

export function treeToReactNode(tree: BuilderNode[], options: RenderOptions = {}): ReactNode {
  return tree.map((node) => renderNode(node, options))
}

/**
 * Returns `{ order: N }` (or `{}` if unset) for the wrapper element of
 * blocks like image / input / spacer / divider that render their `node`
 * style on an inner element. CSS `order` only affects flex / grid items,
 * so emitting it on every block is safe.
 */
export function orderStyle(node: BuilderNode): CSSProperties {
  const order = (node.props.order as string) || ''
  return order ? { order: Number(order) } : {}
}

function renderNode(node: BuilderNode, options: RenderOptions): ReactNode {
  // `data-fk-id` lets the responsive `@media` rules generated by
  // `generateBreakpointCss` target each node by id.
  const idAttr = { 'data-fk-id': node.id }
  switch (node.type) {
    case 'container':
      return (
        <div key={node.id} {...idAttr} style={containerStyle(node)}>
          {node.children.length > 0
            ? node.children.map((c) => renderNode(c, options))
            : null}
        </div>
      )

    // `flex` and `grid` are legacy block types. Trees saved before they were
    // collapsed into Div still reference them; both render through divStyle
    // which respects the `display` prop (forced to flex/grid here for back-
    // compat), so old data renders identically.
    case 'flex':
    case 'grid':
    case 'div':
      return (
        <div key={node.id} {...idAttr} style={divStyle(node)}>
          {node.children.length > 0
            ? node.children.map((c) => renderNode(c, options))
            : null}
        </div>
      )

    case 'card':
      return (
        <div key={node.id} {...idAttr} style={cardStyle(node)}>
          {node.children.map((c) => renderNode(c, options))}
        </div>
      )

    case 'card-header':
      return (
        <div key={node.id} {...idAttr} style={cardHeaderStyle(node)}>
          {node.children.map((c) => renderNode(c, options))}
        </div>
      )

    case 'card-content':
      return (
        <div key={node.id} {...idAttr} style={cardContentStyle(node)}>
          {node.children.map((c) => renderNode(c, options))}
        </div>
      )

    case 'card-footer':
      return (
        <div key={node.id} {...idAttr} style={cardFooterStyle(node)}>
          {node.children.map((c) => renderNode(c, options))}
        </div>
      )

    case 'heading': {
      const level = (node.props.level as string) ?? '2'
      const Tag = (`h${level}` as 'h1' | 'h2' | 'h3' | 'h4')
      return (
        <Tag key={node.id} {...idAttr} style={headingStyle(node)}>
          {node.content ?? ''}
        </Tag>
      )
    }

    case 'text':
      return (
        <p key={node.id} {...idAttr} style={textStyle(node)}>
          {node.content ?? ''}
        </p>
      )

    case 'image':
      return (
        <div
          key={node.id}
          {...idAttr}
          style={{
            display: 'flex',
            justifyContent: imageJustify(node),
            ...orderStyle(node),
          }}
        >
          <img
            src={(node.props.src as string) || ''}
            alt={(node.props.alt as string) || ''}
            style={imageStyle(node)}
          />
        </div>
      )

    case 'spacer':
      return (
        <div
          key={node.id}
          {...idAttr}
          style={{
            height: (node.props.height as string) || '16px',
            ...orderStyle(node),
          }}
        />
      )

    case 'divider':
      return (
        <hr
          key={node.id}
          {...idAttr}
          style={{
            border: 'none',
            borderTop: `${(node.props.thickness as string) || '1px'} solid ${
              (node.props.color as string) || '#e5e7eb'
            }`,
            width: (node.props.width as string) || '100%',
            margin: 0,
            ...orderStyle(node),
          }}
        />
      )

    case 'button':
      return (
        <a
          key={node.id}
          {...idAttr}
          href={(node.props.href as string) || '#'}
          style={buttonStyle(node)}
        >
          {node.content ?? 'Button'}
        </a>
      )

    case 'submit_button':
      // In the runtime portal, the submit button drives the host <form>'s
      // onSubmit handler. In the builder preview it stays a non-interactive
      // link so it visually matches the regular button without firing nav.
      return options.runtime ? (
        <button
          key={node.id}
          {...idAttr}
          type='submit'
          style={{ ...buttonStyle(node), border: 'none', cursor: 'pointer' }}
        >
          {node.content ?? 'Submit'}
        </button>
      ) : (
        <a key={node.id} {...idAttr} href='#' style={buttonStyle(node)}>
          {node.content ?? 'Submit'}
        </a>
      )

    // Magic link / passkey: rendered as alternative auth actions. The host
    // portal page wires the click behavior by selecting on the
    // `data-fk-action` attribute (so the renderer stays declarative and the
    // builder preview is still inert). `type="button"` keeps them from
    // submitting the surrounding <form> by accident.
    case 'magic_link_button':
      return (
        <button
          key={node.id}
          {...idAttr}
          type='button'
          data-fk-action='magic-link'
          style={{
            ...buttonStyle(node),
            border: buttonStyle(node).border ?? 'none',
            cursor: options.runtime ? 'pointer' : 'default',
          }}
          disabled={!options.runtime}
        >
          {node.content ?? 'Sign in with a magic link'}
        </button>
      )

    case 'passkey_button':
      return (
        <button
          key={node.id}
          {...idAttr}
          type='button'
          data-fk-action='passkey'
          style={{
            ...buttonStyle(node),
            border: buttonStyle(node).border ?? 'none',
            cursor: options.runtime ? 'pointer' : 'default',
          }}
          disabled={!options.runtime}
        >
          {node.content ?? 'Sign in with a passkey'}
        </button>
      )

    case 'input':
    case 'email_input':
    case 'password_input':
    case 'totp_input':
      return (
        <div
          key={node.id}
          {...idAttr}
          style={{
            display: 'flex',
            flexDirection: 'column',
            gap: 6,
            ...orderStyle(node),
          }}
        >
          {node.props.label ? (
            <label style={inputLabelStyle()}>{node.props.label as string}</label>
          ) : null}
          <input
            type={resolveInputType(node)}
            name={resolveInputName(node)}
            placeholder={(node.props.placeholder as string) || ''}
            style={inputFieldStyle()}
            disabled={!options.runtime}
          />
          {node.props.helperText ? (
            <span style={inputHelperStyle()}>{node.props.helperText as string}</span>
          ) : null}
        </div>
      )

    case 'page-content':
      return (
        <div key={node.id} data-portal-page-content>
          {options.pageContent ?? null}
        </div>
      )

    case 'identity_providers':
      return (
        <IdentityProvidersBlock
          key={node.id}
          node={node}
          providers={options.identityProviders}
          runtime={options.runtime}
        />
      )

    default:
      return null
  }
}

const PLACEHOLDER_PROVIDERS: Pick<
  Schemas.IdentityProviderPresentation,
  'id' | 'display_name' | 'icon' | 'kind' | 'login_url'
>[] = [
  { id: 'preview-google', display_name: 'Google', icon: 'google', kind: 'google', login_url: '#' },
  { id: 'preview-github', display_name: 'GitHub', icon: 'github', kind: 'github', login_url: '#' },
]

const isAbsoluteUrl = (value: string) => /^https?:\/\//i.test(value)

function buildProviderLoginUrl(loginUrl: string): string {
  const base = window.apiUrl.endsWith('/') ? window.apiUrl : `${window.apiUrl}/`
  const path = loginUrl.replace(/^\//, '')
  const url = new URL(isAbsoluteUrl(loginUrl) ? loginUrl : path, base)
  const currentParams = new URLSearchParams(window.location.search)
  currentParams.forEach((value, key) => {
    if (!url.searchParams.has(key)) {
      url.searchParams.set(key, value)
    }
  })
  return url.toString()
}

export function IdentityProvidersBlock({
  node,
  providers,
  runtime,
}: {
  node: BuilderNode
  providers?: Schemas.IdentityProviderPresentation[]
  runtime?: boolean
}) {
  const separatorLabel = (node.props.separatorLabel as string) || 'Or continue with'
  const buttonLabelPrefix = (node.props.buttonLabel as string) || 'Continue with'

  // In the canvas (or when the realm has no providers yet) we render two
  // sample buttons so the admin still sees what the block will look like.
  const list = runtime
    ? (providers ?? [])
    : (providers && providers.length > 0 ? providers : PLACEHOLDER_PROVIDERS)

  if (runtime && list.length === 0) {
    // At runtime, an empty list collapses the whole block so the surrounding
    // layout doesn't show an orphan "Or continue with" separator.
    return null
  }

  return (
    <div
      data-fk-id={node.id}
      style={{
        display: 'flex',
        flexDirection: 'column',
        gap: 12,
        ...orderStyle(node),
      }}
    >
      <div style={providerSeparatorStyle()}>
        <span style={providerSeparatorLineStyle()} />
        <span style={providerSeparatorLabelStyle()}>{separatorLabel}</span>
        <span style={providerSeparatorLineStyle()} />
      </div>
      <div style={{ display: 'grid', gap: 12 }}>
        {list.map((provider) => {
          const iconKey = provider.icon?.toLowerCase()
          const knownIcon = iconKey && isProviderIconKey(iconKey) ? iconKey : null
          const customIconSrc = iconKey && !knownIcon ? provider.icon : null
          const href =
            runtime && provider.login_url && provider.login_url !== '#'
              ? buildProviderLoginUrl(provider.login_url)
              : '#'
          return (
            <a
              key={provider.id}
              href={href}
              style={providerButtonStyle()}
              onClick={runtime ? undefined : (e) => e.preventDefault()}
            >
              <span style={providerIconSlotStyle()}>
                {knownIcon ? (
                  <ProviderIcon icon={knownIcon} size='sm' className='h-4 w-4' />
                ) : customIconSrc ? (
                  <img
                    src={customIconSrc}
                    alt={provider.display_name ?? ''}
                    style={{ width: 16, height: 16 }}
                  />
                ) : (
                  <span style={providerIconFallbackStyle()}>
                    {provider.display_name?.[0]?.toUpperCase() ?? '?'}
                  </span>
                )}
              </span>
              <span style={providerLabelStyle()}>
                {buttonLabelPrefix} {provider.display_name}
              </span>
              <span style={providerArrowStyle()}>→</span>
            </a>
          )
        })}
      </div>
    </div>
  )
}

